package lmu.hradio.hradioshowcase.spotify.web.model;

import java.io.Serializable;

public abstract class PlayableItemList implements Serializable {

    private static final long serialVersionUID = -2598278910261871947L;
    private String href;
    private int limit;
    private int offset;
    private int total;
    private String next;
    private String previous;

    public String getHref() {
        return href;
    }

    public void setHref(String href) {
        this.href = href;
    }

    public int getLimit() {
        return limit;
    }

    public void setLimit(int limit) {
        this.limit = limit;
    }

    public int getOffset() {
        return offset;
    }

    public void setOffset(int offset) {
        this.offset = offset;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public String getNext() {
        return next;
    }

    public void setNext(String next) {
        this.next = next;
    }

    public String getPrevious() {
        return previous;
    }

    public void setPrevious(String previous) {
        this.previous = previous;
    }
}

